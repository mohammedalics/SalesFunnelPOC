CREATE VIEW SF.VW_STAGES AS select '1-25%' as stage from dual
union all
select '25-50%' from dual
union all
select '50-75%' from dual
union all
select '75-99%' from dual;

CREATE VIEW SF.VW_QUARTERS AS select to_char(sysdate,'YYYY\')||'1' q from dual
union all
select to_char(sysdate,'YYYY\')||'2' from dual
union all
select to_char(sysdate,'YYYY\')||'3' from dual
union all
select to_char(sysdate,'YYYY\')||'4' from dual;

CREATE VIEW SF.VW_OPP AS select
  case 
    when opp_status ='WON' then '99-WON'
    when opp_probability < 25 then '1-25%'
    when opp_probability < 50 then '25-50%'
    when opp_probability < 75 then '50-75%'
    else '75-99%'
  end as stage,
  opp_estimated_amount,
  opp_estimated_amount*(opp_probability/100) as probable_amount,
  emp_id,
  expected_close_date,
  to_char(expected_close_date,'YYYY\Q') expected_close_quarter,
  opp_status,
  close_date,
 to_char(close_date,'YYYY\Q') close_quarter,
 acc_id
from opportunities opp
WHERE opp.opp_status in ('LEAD','OPP','WON');

CREATE VIEW SF.VW_EMP_TARGET AS with opp as 
  (select emp_id,sum(opp.opp_estimated_amount) closed_amount from opportunities opp
   where trunc(opp.close_date,'YEAR')=trunc(sysdate,'YEAR') group by emp_id)
select emp.emp_id,opp.closed_amount,emp.emp_yearly_target_amount target_amount from employees emp 
left join opp on emp.emp_id=opp.emp_id;

