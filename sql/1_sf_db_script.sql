CREATE VIEW SF.GROUPMEMBERS AS select username g_member, 'employees' g_name
from employees
union all
select username g_member, 'managers' g_name
from employees emp where exists (select 1 from employees em1 where em1.manager_id=emp.emp_id);

CREATE VIEW SF.VW_OPP_EMP AS with opp as (
SELECT
  EMP_ID,
  STAGE,
  SUM(OPP_ESTIMATED_AMOUNT) OPP_ESTIMATED_AMOUNT,
  SUM(PROBABLE_AMOUNT) PROBABLE_AMOUNT
FROM 
  VW_OPP GROUP BY EMP_ID, STAGE
ORDER BY STAGE)
select 
emp.emp_id,
emp.stage,
opp.OPP_ESTIMATED_AMOUNT,
opp.PROBABLE_AMOUNT,
/*case 
  when stage='1-25%' then emp.emp_yearly_target_amount/0.25
  when stage='25-50%' then emp.emp_yearly_target_amount/0.5
  when stage='50-75%' then emp.emp_yearly_target_amount/0.75
  when stage='75-99%' then emp.emp_yearly_target_amount/0.95
  else emp_yearly_target_amount
end / 4 as stage_target -- 4 is a constant - length of sales cycle
*/
emp_yearly_target_amount stage_target
from (select * from vw_stages
cross join employees) emp 
left join opp on opp.stage=emp.stage and emp.emp_id=opp.emp_id;

CREATE VIEW SF.VW_OPP_COUNTRIES AS with opp as (select
  acc_country,
  opportunities.stage,
  sum(opportunities.opp_estimated_amount) opp_estimated_amount
FROM 
    VW_OPP OPPORTUNITIES JOIN ACCOUNTS ACCOUNTS1 USING (ACC_ID) 
    WHERE
OPPORTUNITIES.OPP_STATUS IN ('LEAD','OPPORTUNITY')
group by acc_country, opportunities.stage )

select acc_country,
  max(decode( stage, '1-25%', opp_estimated_amount, NULL )) "1-25%",
  max(decode( stage, '25-50%', opp_estimated_amount, NULL )) "25-50%",
 max(decode( stage, '50-75%', opp_estimated_amount, NULL )) "50-75%",
 max(decode( stage, '75-99%', opp_estimated_amount, NULL )) "75-99%"
from opp t
group by acc_country;

CREATE VIEW SF.VW_EMP_EXPECTED_CLOSE_Q AS with opp as (
select 
  sum(probable_amount) probable_amount,
  emp_id,
  expected_close_quarter
from 
  vw_opp 
where opp_status in ('LEAD','OPP') group by emp_id, expected_close_quarter
)
select
opp."PROBABLE_AMOUNT",opp."EMP_ID",opp."EXPECTED_CLOSE_QUARTER",
emp.emp_yearly_target_amount/4 emp_quarterly_target_amount
from opp
inner join employees emp on emp.emp_id=opp.emp_id
order by opp.expected_close_quarter;

CREATE VIEW SF.VW_EMP_CLOSE_Q AS with opp as (
select 
  sum(opp_estimated_amount) closed_amount,
  emp_id,
  close_quarter
from 
  vw_opp 
where opp_status in ('WON') group by emp_id, close_quarter
)
select
opp."CLOSED_AMOUNT",opp."EMP_ID",opp."CLOSE_QUARTER",
emp.emp_yearly_target_amount/4 emp_quarterly_target_amount
from opp
inner join employees emp on emp.emp_id=opp.emp_id
order by opp.close_quarter;

CREATE VIEW SF.VW_EMP_QUARTER_REPORT AS select 
  emp.emp_id,
  emp.q quarter,
  eeq.probable_amount,
  nvl(ecq.closed_amount,0) closed_amount,
  nvl(eeq.emp_quarterly_target_amount,ecq.emp_quarterly_target_amount) emp_quarterly_target_amount
from 
(SELECT emp.emp_id,qts.* FROM vw_quarters qts
cross join employees emp) emp
left join vw_emp_expected_close_q eeq on eeq.emp_id=emp.emp_id and eeq.expected_close_quarter=emp.q
left join vw_emp_close_q ecq on ecq.emp_id=emp.emp_id and ecq.close_quarter=emp.q;

